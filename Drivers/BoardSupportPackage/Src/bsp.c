#include "bsp.h"

/* @brief 用于ADC滤波的滤波器*/
int adcStaticValue[4] = {0, 0, 0, 0};

int BSP_Filter(int adcValue, float k, uint8_t num) {
    int retValue = 0;
    retValue = k * adcValue + (1 - k) * adcStaticValue[num];
    adcStaticValue[num] = adcValue;
    return retValue;
}

